for _ in range(int(input())):
    n = int(input())
    # 9 ... 6 ... 1 = (3...1...)^2
    # 1 ... 6 ... 9 = (1...3...)^2
    # 1 9 6 ... = (14^2=196) ...
    p = 10**(n//2)
    for i in range(n//2):
      print((p+3*10**i)**2) # 1 .. 3
      print((3*p+10**i)**2) # 3.. 1
    print((p+4*p//10)**2) # 14 ..